package com.mkyong.hashing;

public class RowForExcelMerge {

	@Override
	public String toString() {
		return "RowForExcelMerge [societa=" + societa + ", data=" + data + ", centroDiCosto=" + centroDiCosto
				+ ", dipendente=" + dipendente + ", CF=" + CF + ", codice=" + codice + ", desrizione=" + desrizione
				+ ", quantita=" + quantita + ", taglia=" + taglia + "]";
	}
	String societa;
	String data;
	String centroDiCosto;
	String dipendente;
	String CF;
	String codice;
	String desrizione;
	String quantita;
	String taglia;
	public String getSocieta() {
		return societa;
	}
	public void setSocieta(String societa) {
		this.societa = societa;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public String getCentroDiCosto() {
		return centroDiCosto;
	}
	public void setCentroDiCosto(String centroDiCosto) {
		this.centroDiCosto = centroDiCosto;
	}
	public String getDipendente() {
		return dipendente;
	}
	public void setDipendente(String dipendente) {
		this.dipendente = dipendente;
	}
	public String getCF() {
		return CF;
	}
	public void setCF(String cF) {
		CF = cF;
	}
	public String getCodice() {
		return codice;
	}
	public void setCodice(String codice) {
		this.codice = codice;
	}
	public String getDesrizione() {
		return desrizione;
	}
	public void setDesrizione(String desrizione) {
		this.desrizione = desrizione;
	}
	public String getQuantita() {
		return quantita;
	}
	public void setQuantita(String quantita) {
		this.quantita = quantita;
	}
	public String getTaglia() {
		return taglia;
	}
	public void setTaglia(String taglia) {
		this.taglia = taglia;
	}
	
	
}
